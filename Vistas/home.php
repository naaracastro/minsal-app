        <p>
            El Ministerio de Salud fue creado en virtud del Decreto con Fuerza de Ley N°25 de 1959, con la responsabilidad de
            realizar actividades de programación, control y coordinación en materia de salubridad pública. Sin embargo, desde mucho
            tiempo atrás una serie de instituciones tuvieron entre sus tareas el velar por la salud de los chilenos.
            
            Los orígenes habría que buscarlos en 1552 cuando por orden del Rey Carlos V de España se funda el Hospital San Juan de
            Dios, primer establecimiento de salud del país. Durante la época colonial existía alta prevalencia de enfermedades
            infecto-contagiosas, entre ellas viruela, tifus exantemático, sarampión, afecciones entéricas, tuberculosis, enfermedades venéreas y ­en algunas zonas del país- peste bubónica, malaria y fiebre amarilla. El nivel socioeconómico, las condiciones ambientales, la ruralidad y la falta de acceso a los escasos establecimientos asistenciales condicionaban la situación descrita.
        </p>

